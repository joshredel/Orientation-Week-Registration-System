                        <p id="general" class="lead">General</p>
                        <ul>
                            <li>
                                <p><a class="toggler" data-toggle="collapse" data-target="#q1">What is Orientation Week?</a></p>
                                <p id="q1" class="collapse">Orientation Week is a week full of events that takes place the during 9 days leading up to your first day of classes at McGill. It is an excellent opportunity for new students to get to know the University, the city of Montreal, other peers in their Faculty, and upper-year students. </p>
                            </li>
                            
                            <li>
                                <p><a class="toggler" data-toggle="collapse" data-target="#q2">Why should I do Orientation Week?</a></p>
                                <p id="q2" class="collapse">Orientation Week is your time to discover all the great things McGill has to offer. It’s when you’ll get vital information about filling up your semesters with classes, co-curricular activities, and leisure time. Plus, there is no better time to meet people, from support staff, fellow first-years, and upper-year students, through fun and interactive events. The memories you make and people you meet during the week will create a great start to your McGill career. </p>
                            </li>
                            <li>
                                <p><a class="toggler" data-toggle="collapse" data-target="#q21">Who can participate in Orientation Week?</a></p>
                                <p id="q21" class="collapse">The Orientation Week events and programming listed in this website are for all new undergraduate students at McGill University. New students includes students entering their first year of university at McGill, transfer students new to McGill, and exchange students new to McGill. Returning students, family members, and friends of new students that are not also attending McGill for the first time are not permitted to attend Orientation Week events.<br>
                                New graduate students to McGill University should visit Campus Life &amp; Engagement's <a href="http://www.mcgill.ca/firstyear/grad-students/fall-orientation">Grad Student Orientation</a> page for more information.</p>
                            </li>
                            <li>
                                <p><a class="toggler" data-toggle="collapse" data-target="#q3">How much does it cost to participate in Orientation Week? Shouldn’t students under 18 pay less for Frosh?</a></p>
                                <p id="q3" class="collapse">Depending on what you choose to do, participation in Orientation Week can cost as little as completely free. Most individual events are free or have minimal costs for equipment rentals. There is also a cost associated with participating in any Frosh, which covers costs such as admission fees and food. You can click on the WHAT'S INCLUDED button in the Frosh category of the <a href="./events.php">Events</a> page for a more complete list of what the cost covers.<br>
                                Alcohol is provided at a cost on campus and factors in minimally into the Frosh price. For this and other reasons, students of any age are asked to pay the same price. In fact, the increased costs associated with organizing and ensuring the availability of Frosh events that students of all ages can attend would mean that technically students under 18 “cost” the Frosh organizing teams more than those over 18.</p>
                            </li>
                            
                            <li>
                                <p><a class="toggler" data-toggle="collapse" data-target="#q4">What is Campus Life &amp; Engagement?</a></p>
                                <p id="q4" class="collapse">The Campus Life &amp; Engagement (CL&amp;E) team is a part of McGill Student Services and specializes, in part, in the transition into your first year of university. They help you settle in and find the people and services you need to start your university career. They help demystify McGill jargon, assist with registration, prepare you for advising sessions, and match new students with senior students as part of the Student Life Ambassador Program. Campus Life &amp; Engagement is located on the third floor (suite 3100) of the Brown Student Services Building (3600 McTavish Street).</p>
                            </li>
                            
                            <li>
                                <p><a class="toggler" data-toggle="collapse" data-target="#q5">What and where is the Orientation Centre and Resource Room?</a></p>
                                <p id="q5" class="collapse">Campus Life &amp; Engagement's Orientation Centre is the best place to get honest answers about all your questions regarding your transition to McGill. The Centre is staffed by experienced McGill students ready to provide information about clubs, organizations and services at McGill. The Centre is located on the second floor of the Brown Student Services Building (3600 McTavish Street), and will open from 9:00 a.m. to 5:00 p.m., from August 20 to September 5, 2014, closing for weekends and Labour Day.<br /><br />Campus Life &amp; Engagement's First-Year Resource Room offers new students a place to go for assistance and access to telephones and computers for course registration. New this year are drop-in sessions held in the Resource Room where new students can chat with on-campus experts about a variety of subjects. Check the <a href="./events.php">Events</a> page for more information on these sessions.</p>
                            </li>
                            
                            <li>
                                <p><a class="toggler" data-toggle="collapse" data-target="#q6">If I have a question about Orientation Week that isn't listed here, whom can I contact?</a></p>
                                <p id="q6" class="collapse">Check out the <a href="./contact.php">Contact Us</a> page for information on how to contact us and for our handy query form. Make sure to also check out our <a href="https://www.facebook.com/mcgillorientation" target="_blank">Facebook</a> and <a href="https://twitter.com/McGillOWeek" target="_blank">Twitter</a> pages to see if your question has already been answered there.</p>
                            </li>
                            
                            <li>
                                <p><a class="toggler" data-toggle="collapse" data-target="#q61">Can my parents participate in Orientation Week / Frosh?</a></p>
                                <p id="q61" class="collapse">Orientation and Frosh activities are for student participation only. If your parents want to learn more about McGill, we encourage them to check out our McGill Parents tent and Parent Information Sessions. Browse the <a href="./parents.php">Parents</a> section of this website for more information.</p>
                            </li>
                        </ul>
                        
                        <p id="events" class="lead">Events</p>
                        <ul>
                            <li>
                                <p><a class="toggler" data-toggle="collapse" data-target="#q7">Who is hosting events during Orientation Week?</a></p>
                                <p id="q7" class="collapse">Orientation Week events are planned by a combination of Campus Life &amp; Engagement (CL&amp;E), the Students' Society of McGill University (SSMU), Faculty student associations, and different campus clubs, groups, and departments. The <a href="./events.php">Events</a> page's listing includes the name of each event's organizer(s).</p>
                            </li>
                            
                            <li>
                                <p><a class="toggler" data-toggle="collapse" data-target="#q8">How can I find out more about a particular event?</a></p>
                                <p id="q8" class="collapse">Descriptions about each event can be found in the <a href="./events.php">Events</a> page. If there is a (contact us) link next to the organizer's name, you can email the event organizer for more information.</p>
                            </li>
                            
                            <li>
                                <p><a class="toggler" data-toggle="collapse" data-target="#q9">If the event has reached its capacity, what can I do?</a></p>
                                <p id="q9" class="collapse">If the event's registration has been capped, check back frequently to see if spots have opened up because of un-registration. A waiting list function is not available. Some events will allow you to add the event to your calendar; however, the event will still be first-come-first-served.</p>
                            </li>
                            
                            <li>
                                <p><a class="toggler" data-toggle="collapse" data-target="#q10">Can I attend an event if I do not register for it?</a></p>
                                <p id="q10" class="collapse">We ask that you register for all events you intend on attending. Event organizers will receive a list of all registered students. If you come to an event without having pre-registered, the organizer may turn you away, especially if the event capacity has been reached.  Registering will help you track your schedule, provide organizers with attendance information, and secure your spot in a limited registration event.</p>
                            </li>
                            
                            <li>
                                <p><a class="toggler" data-toggle="collapse" data-target="#q11">How do I know where the event is located?</a></p>
                                <p id="q11" class="collapse">The location, or starting location, for every event will be provided to you on your personal account page closer to the event date.</p>
                            </li>
                            
                            <li>
                                <p><a class="toggler" data-toggle="collapse" data-target="#q12">Where is [insert name of location/building here]?</a></p>
                                <p id="q12" class="collapse">Check out our <a href="./map.php">Orientation Week map</a>, see the online <a href="http://www.mcgill.ca/maps/" target="_blank">McGill building map</a>, or visit an information kiosk located on Lower Field.</p>
                            </li>
                            
                            <li>
                                <p><a class="toggler" data-toggle="collapse" data-target="#q14">Is alcohol being served during Orientation Week?</a></p>
                                <p id="q14" class="collapse">The large majority of events during Orientation Week will not be serving alcohol.  However, some events during the week may include the option to purchase alcoholic beverages. Please note the legal drinking age in Quebec is 18 and all establishments and stores will require proof of age to purchase alcohol.  It is also not permitted to sell to nor buy alcohol for persons under the age of 18. Carrying open containers of alcohol in non-designated areas, such as Montreal streets, is illegal and may result in a substantial fine.</p>
                            </li>
                            
                            <li>
                                <p><a class="toggler" data-toggle="collapse" data-target="#q141">I missed an event, is there any way I can still receive some of the information it provided? / I will not be in Montreal by the time Frosh starts, but I would still like to participate. Is that possible?</a></p>
                                <p id="q141" class="collapse">If you will be missing or have missed Discover McGill, you may stop by the Campus Life &amp; Engagement office in the Brown Student Services Building (3600 McTavish Street, Suite 3100).<br><br>If you are unable to attend a Frosh that you signed up for, you may email the Frosh organizers directly and in the case of an extenuating circumstance, a refund may be provided. If you will be unable to attend the beginning of Frosh but would still like to take part, you can email the Frosh organizers and they may be able to provide a means of getting you your bracelet and activity bag so you can participate in the remaining activities. Partial payments/refunds will not be received/given for individual activities.</p>
                            </li>
                        </ul>
                        
                        <p id="frosh" class="lead">Frosh</p>
                        <ul>
                            <li>
                                <p><a class="toggler" data-toggle="collapse" data-target="#q151">Is it possible for me to do a Faculty Frosh outside of my faculty?</a></p>	
                                <p id="q151" class="collapse">Although McGill encourages students to build bonds outside of your faculty, Faculty Froshes are conducted with the purpose of getting to know other students in your faculty and we do not permit you to register for a Frosh outside your faculty. Also, many of the larger activities during Faculty Frosh are done with other faculties, so you will still get the opportunity to meet other new students outside your faculty.  If an extenuating circumstance exists, students may email the outside faculty's Frosh organizers and an exception may be made.</p>
                            </li>
                            
                            <li>
                                <p><a class="toggler" data-toggle="collapse" data-target="#q15">What is the difference between Orientation and Frosh?</a></p>	
                                <p id="q15" class="collapse">Orientation Week is a 9-day period encompassing all the different events for incoming students hosted by a number of groups. Frosh is a 4-day event coordinated by either a club, group, or Faculty student association and is primarily comprised of social activities. Frosh is part of the larger Orientation Week.</p>
                            </li>
                            
                            <li>
                                <p><a class="toggler" data-toggle="collapse" data-target="#q16">I don't know who my Discover McGill and/or Frosh leader is.</a></p>
                                <p id="q16" class="collapse">Discover McGill Leaders are upper-year students in your faculty who will help guide you through the day's activities on August 27. Frosh Leaders are upper-year students who will guide you through the days' activities throughout Frosh. Please note that your Discover McGill Leaders may not end up also being your Frosh Leaders. Closer to Discover McGill Day, you will receive the names of your Discover McGill Leaders and where to meet them the day of.</p>
                            </li>
                            
                            <li>
                                <p><a class="toggler" data-toggle="collapse" data-target="#q17">What if my school/faculty doesn't have a Frosh?</a></p>
                                <p id="q17" class="collapse">If your school/faculty does not have a Frosh, consult the list below.<br />
                                    - Architecture: students participate in Engineering Frosh.<br />
                                    - Arts and Science: students will have the option of either the Arts Frosh or Science Frosh.<br />
                                    - Kinesiology (BSc): students participate in Education Frosh.<br />
                                    - Nursing: students participate in Science Frosh.<br />
                                    - Social Work: students participate in Arts Frosh.<br />
                                    - Religious Studies: students participate in Arts Frosh.<br />
                                    - Pre-Med or Pre-Dent: students participate in Science Frosh.<br><br>
                                    Please note that all Froshes, except Law's, are for undergraduate students only.
                                </p>
                            </li>
                            
                            <li>
                                <p><a class="toggler" data-toggle="collapse" data-target="#q18">Can I do a Faculty Frosh and a Non-Faculty Frosh?</a></p>
                                <p id="q18" class="collapse">Although it is not encouraged, you can do both a Faculty and Non-Faculty Frosh. You will be charged the full price for each Frosh regardless of how many events you attend and may be required to arrange for transportation between the two on your own. Please make sure your Frosh leaders are aware of how you will be dividing your time.</p>
                            </li>
                            
                            <li>
                                <p><a class="toggler" data-toggle="collapse" data-target="#q181">What do I need to bring with me to register for Frosh and/or pick up my Frosh bracelet and bag?</a></p>
                                <p id="q181" class="collapse">Students signing up for Frosh on the Lower Field of McGill’s Downtown Campus should bring cash and a copy of their McGill Student ID card, which can be picked up at the Service Point Building on McTavish.</p>
                            </li>
                        </ul>
                        
                        <p id="registration" class="lead">Registration &amp; Payment</p>
                        <ul>
                            <li>
                                <p><a class="toggler" data-toggle="collapse" data-target="#q19">How can I register for more events after already registering once?</a></p>
                                <p id="q19" class="collapse">Upon registration, you will receive a link to your myWeek account. Make sure to bookmark that link and access your account to continue registering for Orientation Week events.</p>
                            </li>
                            
                            <li>
                                <p><a class="toggler" data-toggle="collapse" data-target="#q20">How can I unregister for events?</a></p>
                                <p id="q20" class="collapse">Upon registration, you will receive a link to your myWeek account. Make sure to bookmark that link and access your account to unregister for any Orientation Week events.</p>
                            </li>
                            
                            <li>
                                <p><a class="toggler" data-toggle="collapse" data-target="#q211">Can I register for two events that overlap in time?</a></p>
                                <p id="q211" class="collapse">Our registration system will allow you to register for two events that overlap in time either entirely or partially. We recommend that for your schedule and to respect the event organizers, you do not plan on attending overlapping events.</p>
                            </li>
                            
                            <li>
                                <p><a class="toggler" data-toggle="collapse" data-target="#q22">If I registered for an event and paid, how can I be refunded if I want to unregister?</a></p>
                                <p id="q22" class="collapse">If you would like to unregister for an event you have already paid for via PayPal, you need to contact the event coordinators for that event.  You can visit the <a href="./events.php">Events</a> page and click on the "(contact us)" part to email them. Although we encourage all students to participate in Frosh, in the event you decide not to participate we advise you to check online for the refund deadline for the Frosh you have signed up for. If it is past this deadline, you will most likely be unable to receive a refund.</p>
                            </li>
                            
                            <li>
                                <p><a class="toggler" data-toggle="collapse" data-target="#q23">How can I register or pay in-person for Frosh and not online? How can I pick up my Frosh items after I have registered online?</a></p>
                                <p id="q23" class="collapse">Frosh registration, payment, and bracelet/bag pick-up can be done in-person on the Lower Field of McGill's downtown campus. Registration booths will be open August 24th-28th from 10am-4pm and the 29th from 10am-12pm. We recommend that you register online for as many events as possible, as spots can fill up quickly. À la carte and Discover McGill events can only be registered for online at this time.</p>
                            </li>
                            
                            <li>
                                <p><a class="toggler" data-toggle="collapse" data-target="#q241">I provided incorrect information when I first registered, how do I go about correcting it?</a></p>
                                <p id="q241" class="collapse">If you send an email to the Orientation Week staff by filling out the form on the <a href="./contact.php">Contact Us</a> page, we can correct the error and ensure you have an awesome Orientation Week!</p>
                            </li>
                        </ul>
                        
