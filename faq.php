                    <h2>Click the questions to see answers to common inquiries</h2>
                    <p>Jump to: <a href="#general">General</a>&nbsp;|&nbsp;<a href="#events">Events</a>&nbsp;|&nbsp;<a href="#frosh">Frosh</a>&nbsp;|&nbsp;<a href="#registration">Registration &amp; Payment</a>&nbsp;|&nbsp;<a href="#social">Website, Social Media &amp; Apps</a></p>
                    
